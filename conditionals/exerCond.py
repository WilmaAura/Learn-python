age = int(input("Enter your age:"))

if age >= 18:
    print("You are old enough to learn to drive")
else:
    print("You need more", 18-age, "to learn to drive")

my_age = 20

if age > my_age:
    print("You are", abs(age-my_age), "older then me")
else:
    print("You are", abs(age-my_age), "younger then me")






